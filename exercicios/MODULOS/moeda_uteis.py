def metade(n):
    s = n/2
    print(f"A metade de R${n} é R${s}.")
    
def dobro(n):
    s = n*2
    print(f"O dobro de R${n} é R${s}.")

def aumentar(n):
    s = n + (n*(13/100))
    print(f"Aumentando R${n} em 13% temos R${s}.")

def diminuir(n):
    s = n - (n*(10/100))
    print(f"Diminuindo {n} em 10% temos {s}.")
